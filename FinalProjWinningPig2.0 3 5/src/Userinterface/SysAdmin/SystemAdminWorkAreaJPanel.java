    private void ManageEnterpriseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ManageEnterpriseBtnActionPerformed
        // TODO add your handling code here:
        
        if(ecoSystem.getNetworkList().size() == 0){
            JOptionPane.showMessageDialog(null, "No Network Created Yet!");
        }else{
            ManageEnterpriseJPanel manageEnterpriseJPanel =new ManageEnterpriseJPanel(userProcessContainer, ecoSystem);
            userProcessContainer.add("manageNetworkJPanel",manageEnterpriseJPanel);
            CardLayout layout=(CardLayout)userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_ManageEnterpriseBtnActionPerformed

    private void ManageEnterpriseAdminBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ManageEnterpriseAdminBtnActionPerformed
        // TODO add your handling code here:
        boolean checkEnterpriseNotEmpty = false;
        for(Network network : ecoSystem.getNetworkList())
            if(network.getEnterpriseDirectory().getEnterpriseList().size() > 0)
                checkEnterpriseNotEmpty = true;
        if(checkEnterpriseNotEmpty){
            ManageEnterpriseAdminJPanel manageEnterpriseAdminJPanel =new ManageEnterpriseAdminJPanel(userProcessContainer, ecoSystem);
            userProcessContainer.add("manageNetworkJPanel",manageEnterpriseAdminJPanel);
            CardLayout layout=(CardLayout)userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_ManageEnterpriseAdminBtnActionPerformed


