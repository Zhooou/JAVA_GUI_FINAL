    private void declineBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_declineBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = requestJTable.getSelectedRow();
        if(selectedRow >= 0){
            BookRequest bookRequest = (BookRequest)requestJTable.getValueAt(selectedRow, 0);
            if(!bookRequest.getStatus().equals("Sent")){
                JOptionPane.showMessageDialog(null, "You cannot change it.");
            }
            else{
                bookRequest.setStatus("Decline");
                JOptionPane.showMessageDialog(null, "Decline Successfully");
                bookRequest.setReceiver(account);
                populateRequest();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
    }//GEN-LAST:event_declineBtnActionPerformed

    private void acceptBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acceptBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = requestJTable.getSelectedRow();
        if(selectedRow >= 0){
            BookRequest bookRequest = (BookRequest)requestJTable.getValueAt(selectedRow, 0);
            if(!bookRequest.getStatus().equals("Sent")){
                JOptionPane.showMessageDialog(null, "You cannot change it.");
            }
            else{
                bookRequest.setStatus("Accept");
                JOptionPane.showMessageDialog(null, "Accept Successfully");
                bookRequest.setReceiver(account);
                for(Book book1 : bookRequest.getBookList())
                    for(Book book2 : enterprise.getBookDirectory().getBookList())
                        if(book1 == book2){
                            int storage = book2.getStorage();
                            book2.setStorage(storage - 1);
                        }
                populateRequest();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
    }//GEN-LAST:event_acceptBtnActionPerformed

