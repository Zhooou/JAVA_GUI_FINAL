    public void populateRequest(){
        BookQueue bookQueue = enterprise.getBookQueue();
        DefaultTableModel model = (DefaultTableModel) requestJTable.getModel();
        
        model.setRowCount(0);
        for (BookRequest bookRequest : bookQueue.getBookRequestList()) {
                Object[] row = new Object[3];
                row[0] = bookRequest;
                row[1] = bookRequest.getSender();
                row[2] = bookRequest.getStatus();
                model.addRow(row);
        }
    }
    
